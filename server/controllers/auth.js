import bcrypt from "bcrypt";
import jwt from "jsonwebtoken";
import User from "../models/User.js";

/* REGISTER USER */
export const register = async (req, res) => {
  try {
    const {
      firstName,
      lastName,
      email,
      password,
      picturePath,
      friends,
      location,
      occupation,
    } = req.body;

    const salt = await bcrypt.genSalt();
    const passwordHash = await bcrypt.hash(password, salt);

    const newUser = new User({
      firstName,
      lastName,
      email,
      password: passwordHash,
      picturePath,
      friends,
      location,
      occupation,
      viewedProfile: Math.floor(Math.random() * 10000),
      impressions: Math.floor(Math.random() * 10000),
    });
    const savedUser = await newUser.save();
    res.status(201).json(savedUser);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};

/* LOGGING IN */
export const login = async (req, res) => {
  try {
    const { email, password } = req.body;
    //use mongoose to finad all the specified information for the email
    const user = await User.findOne({ email: email });

    //if user does not exist
    if (!user) return res.status(400).json({ msg: "User does not exist. " });

    //determine if the password matches by using bcrypt to compare the password the user sent with the saved password in the db
    const isMatch = await bcrypt.compare(password, user.password);

    //theyll both use the same salt.
    if (!isMatch) return res.status(400).json({ msg: "Invalid credentials. " });

    //use jwt token and sign the id with mongodb user._id and pass the secret string
    const token = jwt.sign({ id: user._id }, process.env.JWT_SECRET);
    delete user.password;
    res.status(200).json({ token, user });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};
